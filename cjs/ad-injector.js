window._adFrames = window._adFrames || [];

function render(ads, target, templateFn, getTrackId, getUrl, buildLinkUrl) {
  var linkUrl = buildLinkUrl(ads);
  var el = target ? target : document.createElement('span');
  el.innerHTML = templateFn(Object.assign({
    linkUrl: linkUrl
  }, ads));
  el.setAttribute('data-track-id', getTrackId(ads));
  el.setAttribute('data-orig-link', getUrl(ads));
  el.setAttribute('data-link-url', linkUrl);
  el.setAttribute("data-rendered", "rendered");
  return el;
}

function loadAdFrame(config) {
  var container = config.container;
  var placeholder = config.placeholder,
      extractTrackId = config.extractTrackId,
      extractUrl = config.extractUrl,
      buildLinkUrl = config.buildLinkUrl,
      fetchAds = config.fetchAds,
      onImpression = config.onImpression,
      onFetchError = config.onFetchError;

  if (!extractTrackId) {
    extractTrackId = loadAdFrame.defaults.defaultExtractTrackId;
  }

  if (!extractUrl) {
    extractUrl = loadAdFrame.defaults.defaultExtractUrl;
  }

  if (!buildLinkUrl) {
    buildLinkUrl = loadAdFrame.defaults.defaultBuildLinkUrl;
  }

  if (!fetchAds) {
    fetchAds = loadAdFrame.defaults.defaultFetchAds;
  }

  if (!onImpression) {
    onImpression = loadAdFrame.defaults.defaultOnImpression;
  }

  if (!onFetchError) {
    onFetchError = loadAdFrame.defaults.defaultOnFetchError;
  }

  if (!placeholder) {
    placeholder = PLACEHOLDER_CLASS;
  }

  var cElm = document.querySelector(container);
  if (!cElm) throw new Error("container ".concat(container, " not found."));
  var containerElm = cElm;
  var io = new IntersectionObserver(function (entreis) {
    entreis.forEach(function (entry) {
      if (entry.intersectionRatio === 0) return;
      var target = entry.target;
      var trackId = target.getAttribute('data-track-id');

      if (trackId && !target.getAttribute('data-post-impression')) {
        target.setAttribute('data-post-impression', 'true');
        onImpression(trackId, target).catch(function () {
          target.setAttribute('data-post-impression', '');
        });
      } else if (!trackId) {
        console.error('Invalid track id.');
      }
    });
  });
  fetchAds(containerElm, placeholder).then(function (ads) {
    containerElm.querySelectorAll(placeholder).forEach(function (el) {
      if (el.getAttribute("data-rendered")) return;
      var ad = ads.shift();

      if (ad) {
        var templateId = el.getAttribute('data-template-id') ? el.getAttribute('data-template-id') : config.template;
        var currentTemplate = document.querySelector(templateId);
        if (!currentTemplate) throw new Error("template ".concat(templateId, " not found."));
        var compiled = lodash_template(currentTemplate.innerHTML);
        render(ad, el, compiled, extractTrackId, extractUrl, buildLinkUrl);
        io.observe(el);
      }
    });
  }).catch(function (err) {
    onFetchError(err, containerElm);
  });
}

loadAdFrame.defaults = {
  defaultExtractTrackId: defaultExtractTrackId,
  defaultExtractUrl: defaultExtractUrl,
  defaultBuildLinkUrl: defaultBuildLinkUrl,
  defaultOnImpression: defaultOnImpression,
  defaultFetchAds: defaultFetchAds,
  defaultOnFetchError: defaultOnFetchError
};

function init() {
  window._adFrames.forEach(loadAdFrame);

  window._adFrames = [];
}

init();
var index = {
  loadAdFrame: loadAdFrame
};

module.exports = index;
